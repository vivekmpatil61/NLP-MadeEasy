from googletrans import Translator

def trans(message,lan):
    '''
    This function translates English statements to any language of user choice captured in variable 'lan'
    This method uses state of the art google library named as googletrans. If we don't want to use API calls, then we can build
    translator neural network model and model can predict the outcome but we need lots of relevant data in order to train test validate.
    '''
    translator = Translator()
    result = translator.translate(message, dest=lan)
    st.success(result.text)

